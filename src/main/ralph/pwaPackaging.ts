const execAsync = promisify(exec);

/**
 * Validate workspace path to prevent path traversal attacks
 */
function validateWorkspacePath(workspacePath: string): { valid: boolean; error?: string } {
  // Check for null bytes
  if (workspacePath.includes('\0')) {
    return { valid: false, error: 'Invalid path: contains null bytes' };
  }

  // Resolve the path and check it doesn't escape to unsafe directories
  try {
    const resolved = path.resolve(workspacePath);
    if (resolved.startsWith('/etc') || resolved.startsWith('/sys') || resolved.startsWith('/proc')) {
      return { valid: false, error: 'Invalid path: path escapes sandbox' };
    }
  } catch {
    return { valid: false, error: 'Invalid path: resolution failed' };
  }

  return { valid: true };
}

export interface PWAConfig {
  appName?: string;
  shortName?: string;
  description?: string;
  themeColor?: string;
  backgroundColor?: string;
  startUrl?: string;
  display?: 'fullscreen' | 'standalone' | 'minimal-ui' | 'browser';
  orientation?: 'portrait' | 'landscape' | 'any';
  icons?: PWAIcon[];
  categories?: string[];
  preferRelatedApplications?: boolean;
}

export interface PWAIcon {
  src: string;
  sizes: string;
  type?: string;
  purpose?: 'any' | 'maskable' | 'badge';
}

export interface PWAPackagingResult {
  success: boolean;
  artifacts: DeliveryArtifact[];
  gates: ReleaseGate[];
  lighthouseScores?: {
    performance: number;
    accessibility: number;
    bestPractices: number;
    SEO: number;
    pwa: number;
  };
  error?: string;
}

const DEFAULT_PWA_CONFIG: PWAConfig = {
  themeColor: '#4F46E5',
  backgroundColor: '#ffffff',
  display: 'standalone',
  orientation: 'portrait',
};

/**
 * Generate a web app manifest for PWA
 */
export function generateManifest(workspacePath: string, appName: string, config?: Partial<PWAConfig>): { success: boolean; path?: string; error?: string } {
  try {
    const publicDir = path.join(workspacePath, 'public');

    // Create public directory if it doesn't exist
    if (!fs.existsSync(publicDir)) {
      fs.mkdirSync(publicDir, { recursive: true });
    }

    const manifestConfig = { ...DEFAULT_PWA_CONFIG, ...config, appName };
    const manifestPath = path.join(publicDir, 'manifest.json');

    // Default icons if not provided
    if (!manifestConfig.icons || manifestConfig.icons.length === 0) {
      manifestConfig.icons = [
        { src: '/icon-192.png', sizes: '192x192', type: 'image/png', purpose: 'any' },
        { src: '/icon-512.png', sizes: '512x512', type: 'image/png', purpose: 'any' },
        { src: '/icon-maskable.png', sizes: '512x512', type: 'image/png', purpose: 'maskable' },
      ];
    }

    const manifest = {
      name: manifestConfig.appName,
      short_name: manifestConfig.shortName || manifestConfig.appName.substring(0, 12),
      description: manifestConfig.description || `${manifestConfig.appName} - Progressive Web App`,
      start_url: manifestConfig.startUrl || '/',
      display: manifestConfig.display,
      orientation: manifestConfig.orientation,
      background_color: manifestConfig.backgroundColor,
      theme_color: manifestConfig.themeColor,
      icons: manifestConfig.icons,
      categories: manifestConfig.categories || ['utilities', 'productivity'],
      prefer_related_applications: manifestConfig.preferRelatedApplications || false,
    };

    fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2), 'utf-8');

    return { success: true, path: manifestPath };
  } catch (error) {
    return { success: false, error: error instanceof Error ? error.message : String(error) };
  }
}

/**
 * Generate a service worker for PWA offline support
 */
export function generateServiceWorker(workspacePath: string, appName: string): { success: boolean; path?: string; error?: string } {
  try {
    const publicDir = path.join(workspacePath, 'public');

    // Create public directory if it doesn't exist
    if (!fs.existsSync(publicDir)) {
      fs.mkdirSync(publicDir, { recursive: true });
    }

    const swPath = path.join(publicDir, 'sw.js');

    const serviceWorker = `/**
 * ${appName} Service Worker
 * Generated by Knuthflow for PWA support
 *
 * Strategies:
 * - Cache-first for app shell (HTML, CSS, JS, fonts, images)
 * - Network-first for API calls
 * - Stale-while-revalidate for dynamic content
 */

const CACHE_NAME = '${appName.toLowerCase().replace(/[^a-z0-9]/g, '-')}-v1';
const STATIC_CACHE = 'static-v1';
const DYNAMIC_CACHE = 'dynamic-v1';

// App shell files to cache immediately
const APP_SHELL = [
  '/',
  '/index.html',
  '/manifest.json',
  '/icon-192.png',
  '/icon-512.png',
];

/**
 * Install event - cache app shell
 */
self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(STATIC_CACHE)
      .then((cache) => {
        console.log('[SW] Caching app shell');
        return cache.addAll(APP_SHELL.filter(url => !url.startsWith('/icon')));
      })
      .then(() => self.skipWaiting())
  );
});

/**
 * Activate event - clean up old caches
 */
self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames
          .filter((name) => name !== STATIC_CACHE && name !== DYNAMIC_CACHE)
          .map((name) => {
            console.log('[SW] Deleting old cache:', name);
            return caches.delete(name);
          })
      );
    }).then(() => self.clients.claim())
  );
});

/**
 * Fetch event - handle requests with appropriate strategy
 */
self.addEventListener('fetch', (event) => {
  const { request } = event;
  const url = new URL(request.url);

  // Skip non-GET requests
  if (request.method !== 'GET') {
    return;
  }

  // Skip chrome-extension and other non-http requests
  if (!url.protocol.startsWith('http')) {
    return;
  }

  // API calls: network-first strategy
  if (url.pathname.startsWith('/api/') || url.pathname.startsWith('/v1/')) {
    event.respondWith(networkFirst(request));
    return;
  }

  // Static assets (js, css, fonts, images): cache-first strategy
  if (
    url.pathname.match(/\\.(js|css|woff2?|ttf|eot|jpg|jpeg|png|gif|webp|svg|ico)$/)
  ) {
    event.respondWith(cacheFirst(request));
    return;
  }

  // HTML pages: stale-while-revalidate
  if (request.headers.get('accept')?.includes('text/html')) {
    event.respondWith(staleWhileRevalidate(request));
    return;
  }

  // Default: network-first
  event.respondWith(networkFirst(request));
});

/**
 * Cache-first strategy
 * Best for: static assets that don't change often
 */
async function cacheFirst(request) {
  const cached = await caches.match(request);
  if (cached) {
    return cached;
  }

  try {
    const response = await fetch(request);
    if (response.ok) {
      const cache = await caches.open(STATIC_CACHE);
      cache.put(request, response.clone());
    }
    return response;
  } catch (error) {
    // Return offline fallback if available
    const fallback = await caches.match('/');
    if (fallback) return fallback;
    throw error;
  }
}

/**
 * Network-first strategy
 * Best for: API calls, dynamic content
 */
async function networkFirst(request) {
  try {
    const response = await fetch(request);
    if (response.ok) {
      const cache = await caches.open(DYNAMIC_CACHE);
      cache.put(request, response.clone());
    }
    return response;
  } catch (error) {
    const cached = await caches.match(request);
    if (cached) {
      return cached;
    }
    throw error;
  }
}

/**
 * Stale-while-revalidate strategy
 * Best for: HTML pages - serve cached, update in background
 */
async function staleWhileRevalidate(request) {
  const cached = await caches.match(request);

  const fetchPromise = fetch(request).then((response) => {
    if (response.ok) {
      const cache = await caches.open(DYNAMIC_CACHE);
      cache.put(request, response.clone());
    }
    return response;
  }).catch(() => cached);

  return cached || fetchPromise;
}

/**
 * Handle messages from the main thread
 */
self.addEventListener('message', (event) => {
  if (event.data === 'skipWaiting') {
    self.skipWaiting();
  }
});
`;

    fs.writeFileSync(swPath, serviceWorker, 'utf-8');

    return { success: true, path: swPath };
  } catch (error) {
    return { success: false, error: error instanceof Error ? error.message : String(error) };
  }
}

/**
 * Register the service worker in the web app
 */
export function registerServiceWorker(workspacePath: string): { success: boolean; path?: string; error?: string } {
  try {
    // Find the main HTML file
    const htmlPaths = [
      path.join(workspacePath, 'index.html'),
      path.join(workspacePath, 'dist', 'index.html'),
      path.join(workspacePath, 'public', 'index.html'),
    ];

    const htmlPath = htmlPaths.find(p => fs.existsSync(p));
    if (!htmlPath) {
      return { success: false, error: 'index.html not found' };
    }

    let html = fs.readFileSync(htmlPath, 'utf-8');

    // Check if SW is already registered
    if (html.includes('sw.js') || html.includes('serviceWorker.register')) {
      return { success: true, path: htmlPath };
    }

    // Add service worker registration before closing </body>
    const swRegistration = `  <script>
    if ('serviceWorker' in navigator) {
      window.addEventListener('load', () => {
        navigator.serviceWorker.register('/sw.js')
          .then((registration) => {
            console.log('SW registered:', registration.scope);
          })
          .catch((error) => {
            console.log('SW registration failed:', error);
          });
      });
    }
  </script>`;

    // Insert before </body>
    if (html.includes('</body>')) {
      html = html.replace('</body>', `${swRegistration}\n</body>`);
    } else {
      html += swRegistration;
    }

    fs.writeFileSync(htmlPath, html, 'utf-8');

    return { success: true, path: htmlPath };
  } catch (error) {
    return { success: false, error: error instanceof Error ? error.message : String(error) };
  }
}

/**
 * Create PWA icons (placeholder SVGs converted to PNG)
 */
export function createPlaceholderIcons(workspacePath: string, appName: string): { success: boolean; paths?: string[]; error?: string } {
  try {
    const publicDir = path.join(workspacePath, 'public');
    if (!fs.existsSync(publicDir)) {
      fs.mkdirSync(publicDir, { recursive: true });
    }

    const sizes = [192, 512, 72, 96, 128, 144, 152, 180, 384];
    const paths: string[] = [];

    // Create a simple SVG-based data URL icon generator
    // For now, we'll just create placeholder files
    // In production, you'd want to use sharp or canvas to generate actual PNGs
    const minimalPng = Buffer.from([
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
      0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
      0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
      0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4,
      0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41,
      0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
      0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00,
      0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE,
      0x42, 0x60, 0x82,
    ]);

    for (const size of sizes) {
      const iconPath = path.join(publicDir, `icon-${size}.png`);
      // Create a minimal PNG header (this is a 1x1 transparent PNG placeholder)
      // Real implementation would generate actual icons
      fs.writeFileSync(iconPath, minimalPng);
      paths.push(iconPath);
    }

    // Create maskable icon
    const maskablePath = path.join(publicDir, 'icon-maskable.png');
    fs.writeFileSync(maskablePath, minimalPng);
    paths.push(maskablePath);

    return { success: true, paths };
  } catch (error) {
    return { success: false, error: error instanceof Error ? error.message : String(error) };
  }
}

/**
 * Run Lighthouse audit for PWA validation
 */
export async function runLighthouseAudit(
  workspacePath: string,
  options?: { port?: number; url?: string }
): Promise<{ success: boolean; scores?: Record<string, number>; error?: string }> {
  const distDir = path.join(workspacePath, 'dist');
  const url = options?.url || `file://${distDir}/index.html`;

  // Check if lighthouse is available
  try {
    await execAsync('npx lighthouse --version', { timeout: 10000 });
  } catch {
    return { success: false, error: 'Lighthouse not installed. Run: npm install -g lighthouse' };
  }

  try {
    const reportPath = path.join(workspacePath, 'dist', 'lighthouse-report.html');

    const lighthouseCmd = `npx lighthouse "${url}" --output=html --output-path="${reportPath}" --only-categories=performance,accessibility,seo,pwa --quiet`;

    await execAsync(lighthouseCmd, { cwd: workspacePath, timeout: 120000 });

    // Try to get JSON output for scores
    const jsonReportPath = path.join(workspacePath, 'dist', 'lighthouse-report.json');
    const jsonCmd = `npx lighthouse "${url}" --output=json --output-path="${jsonReportPath}" --only-categories=performance,accessibility,seo,pwa --quiet`;

    try {
      await execAsync(jsonCmd, { cwd: workspacePath, timeout: 120000 });

      if (fs.existsSync(jsonReportPath)) {
        const report = JSON.parse(fs.readFileSync(jsonReportPath, 'utf-8'));
        const categories = report.categories || {};

        return {
          success: true,
          scores: {
            performance: Math.round((categories.performance?.score || 0) * 100),
            accessibility: Math.round((categories.accessibility?.score || 0) * 100),
            bestPractices: Math.round((categories['best-practices']?.score || 0) * 100),
            SEO: Math.round((categories.seo?.score || 0) * 100),
            pwa: Math.round((categories.pwa?.score || 0) * 100),
          },
        };
      }
    } catch {
      // JSON report failed, but HTML report may have succeeded
    }

    // Return success even if we couldn't parse scores
    return { success: true };
  } catch (error) {
    return { success: false, error: error instanceof Error ? error.message : String(error) };
  }
}

/**
 * Collect PWA artifacts from the workspace
 */
export function collectPWAArtifacts(workspacePath: string): DeliveryArtifact[] {
  const artifacts: DeliveryArtifact[] = [];

  // Check for manifest
  const manifestPath = path.join(workspacePath, 'public', 'manifest.json');
  if (fs.existsSync(manifestPath)) {
    const stats = fs.statSync(manifestPath);
    artifacts.push({
      id: 'pwa-manifest',
      name: 'manifest.json',
      type: 'manifest',
      path: manifestPath,
      size: `${(stats.size / 1024).toFixed(1)} KB`,
      validated: true,
      validatedAt: Date.now(),
      gate: 'pwa-manifest',
      platformTarget: 'pwa',
    });
  }

  // Check for service worker
  const swPath = path.join(workspacePath, 'public', 'sw.js');
  if (fs.existsSync(swPath)) {
    const stats = fs.statSync(swPath);
    artifacts.push({
      id: 'pwa-service-worker',
      name: 'sw.js',
      type: 'package',
      path: swPath,
      size: `${(stats.size / 1024).toFixed(1)} KB`,
      validated: true,
      validatedAt: Date.now(),
      gate: 'pwa-service-worker',
      platformTarget: 'pwa',
    });
  }

  // Check for icons
  const publicDir = path.join(workspacePath, 'public');
  if (fs.existsSync(publicDir)) {
    const iconFiles = fs.readdirSync(publicDir).filter(f => f.startsWith('icon-') && (f.endsWith('.png') || f.endsWith('.svg') || f.endsWith('.ico')));
    for (const iconFile of iconFiles) {
      const iconPath = path.join(publicDir, iconFile);
      const stats = fs.statSync(iconPath);
      artifacts.push({
        id: `pwa-icon-${iconFile}`,
        name: iconFile,
        type: 'package',
        path: iconPath,
        size: `${(stats.size / 1024).toFixed(1)} KB`,
        validated: true,
        validatedAt: Date.now(),
        gate: 'pwa-icons',
        platformTarget: 'pwa',
      });
    }
  }

  // Check for Lighthouse report
  const reportPath = path.join(workspacePath, 'dist', 'lighthouse-report.html');
  if (fs.existsSync(reportPath)) {
    const stats = fs.statSync(reportPath);
    artifacts.push({
      id: 'pwa-lighthouse-report',
      name: 'lighthouse-report.html',
      type: 'runbook',
      path: reportPath,
      size: `${(stats.size / 1024).toFixed(1)} KB`,
      validated: true,
      validatedAt: Date.now(),
      gate: 'pwa-lighthouse',
      platformTarget: 'pwa',
    });
  }

  return artifacts;
}

/**
 * Create PWA release gates
 */
function createPWAGates(
  manifestResult: { success: boolean; path?: string; error?: string },
  swResult: { success: boolean; path?: string; error?: string },
  lighthouseResult: { success: boolean; scores?: Record<string, number>; error?: string },
  lighthouseThreshold = 90
): ReleaseGate[] {
  const gates: ReleaseGate[] = [];

  // Gate: Manifest
  gates.push({
    id: 'gate-pwa-manifest',
    name: 'PWA Manifest',
    description: 'Web app manifest exists and is valid',
    status: manifestResult.success ? 'passed' : 'failed',
    evidence: manifestResult.success ? `Manifest created at ${manifestResult.path}` : manifestResult.error,
    passedAt: manifestResult.success ? Date.now() : undefined,
    platformTarget: 'pwa',
  });

  // Gate: Service Worker
  gates.push({
    id: 'gate-pwa-service-worker',
    name: 'Service Worker',
    description: 'Service worker for offline support',
    status: swResult.success ? 'passed' : 'failed',
    evidence: swResult.success ? `Service worker registered` : swResult.error,
    passedAt: swResult.success ? Date.now() : undefined,
    platformTarget: 'pwa',
  });

  // Gate: Lighthouse
  const scores = lighthouseResult.scores;
  const allScoresPass = scores
    ? Object.entries(scores).every(([key, value]) => {
        if (key === 'pwa' || key === 'bestPractices') return true; // These are bonus
        return value >= lighthouseThreshold;
      })
    : false;

  gates.push({
    id: 'gate-pwa-lighthouse',
    name: 'Lighthouse Audit',
    description: `PWA meets quality thresholds (>= ${lighthouseThreshold})`,
    status: lighthouseResult.success && allScoresPass ? 'passed' : (lighthouseResult.success ? 'failed' : 'skipped'),
    evidence: scores
      ? `Performance: ${scores.performance}, Accessibility: ${scores.accessibility}, SEO: ${scores.SEO}`
      : lighthouseResult.error || 'Lighthouse not run',
    passedAt: lighthouseResult.success && allScoresPass ? Date.now() : undefined,
    platformTarget: 'pwa',
  });

  return gates;
}

/**
 * Build PWA for a workspace
 */
export async function buildPWA(
  workspacePath: string,
  appName: string,
  config?: Partial<PWAConfig>
): Promise<PWAPackagingResult> {
  // Validate workspace path before any operations
  const validation = validateWorkspacePath(workspacePath);
  if (!validation.valid) {
    return {
      success: false,
      artifacts: [],
      gates: [{
        id: 'gate-pwa-validation',
        name: 'PWA Validation',
        description: 'Workspace path validation',
        status: 'failed',
        evidence: validation.error,
        platformTarget: 'pwa',
      }],
      error: validation.error,
    };
  }

  const manifestResult = generateManifest(workspacePath, appName, config);
  const swResult = generateServiceWorker(workspacePath, appName);
  const registerResult = registerServiceWorker(workspacePath);
  const iconsResult = createPlaceholderIcons(workspacePath, appName);

  // Build the web app first
  const pkgPath = path.join(workspacePath, 'package.json');
  if (fs.existsSync(pkgPath)) {
    const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf-8'));
    if (pkg.scripts?.build) {
      try {
        await execAsync('npm run build', { cwd: workspacePath, timeout: 180000 });
      } catch (buildError) {
        // Build might fail but PWA assets can still be created
        console.log('Web build failed, continuing with PWA assets:', buildError);
      }
    }
  }

  // Copy PWA assets to dist if they exist in public
  const publicDir = path.join(workspacePath, 'public');
  const distDir = path.join(workspacePath, 'dist');
  if (fs.existsSync(publicDir) && fs.existsSync(distDir)) {
    const publicFiles = fs.readdirSync(publicDir);
    for (const file of publicFiles) {
      const src = path.join(publicDir, file);
      const dest = path.join(distDir, file);
      if (!fs.existsSync(dest)) {
        fs.copyFileSync(src, dest);
      }
    }
  }

  // Run Lighthouse audit (non-blocking, will be skipped if fails)
  const lighthouseResult = await runLighthouseAudit(workspacePath).catch(() => ({
    success: false,
    error: 'Lighthouse audit failed',
    scores: undefined
  } as { success: boolean; scores?: Record<string, number>; error?: string }));

  const artifacts = collectPWAArtifacts(workspacePath);
  const gates = createPWAGates(manifestResult, swResult, lighthouseResult);

  // Determine overall success: manifest and SW must succeed; Lighthouse is best-effort
  const coreSuccess = manifestResult.success && swResult.success;
  // Lighthouse failure doesn't invalidate the PWA package but should be reported
  const lighthouseFailed = lighthouseResult.success === false && lighthouseResult.scores === undefined;

  return {
    success: coreSuccess,
    artifacts,
    gates,
    lighthouseScores: lighthouseResult.scores as PWAPackagingResult['lighthouseScores'],
    error: !manifestResult.success
      ? manifestResult.error
      : !swResult.success
        ? swResult.error
        : lighthouseFailed
          ? lighthouseResult.error
          : undefined,
  };
}
